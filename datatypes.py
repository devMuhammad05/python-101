

# str - text type
# int, float, complex - numeric types
# list, tuple, range - sequence types

#primitie types - strings, numbers(int, float, complex) and bool

x = "hello"  # str
print(x, "is type of", type(x))

z = 20  # int
print(z, "is type of", type(z))

e = 56.67  # flaot
print(e, "is type of", type(e))

w = 2j  # complex
print(w, "is type of", type(w))

li = ["apple", "orange", "banana"]  # list
print(li, "is type of", type(li))


tuple_example = ("apple", "orange", "banana")  # tuple
print(tuple_example, "is type of", type(tuple_example))

r = range(6)
print(r)
print(r, "is type of", type(r))


s = {"apple", "orange", "cherry"}  # set
print(s, "is type of", type(s))

d = {"name": "Muhammad", "fruit": "cherry"}  # dict
print(d, "is type of", type(d))


b = b"Hello" #bytes 
print(b, "is type of", type(b))


zz = bytearray(5)
print(zz, "is type of", type(zz))                            

